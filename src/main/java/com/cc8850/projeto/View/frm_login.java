    public void carregaPrivilegios() {
        ControllerLogin srvLogin = new ControllerLogin();
        cbxPrivilegio.removeAllItems();
        ArrayList<String> privilegios = srvLogin.getPrivilegios();
        for (int i = 0; i < privilegios.size(); i++) {
            cbxPrivilegio.addItem(privilegios.get(i));
        }
        cbxPrivilegio.setSelectedItem(null);
    }

