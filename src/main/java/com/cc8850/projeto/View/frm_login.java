    private void btOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOKActionPerformed
        // TODO add your handling code here:
        ControllerLogin ctrLogin = new ControllerLogin();
        String sPrivilegio = "";
        if (cbxPrivilegio.getSelectedItem() != null) {
            sPrivilegio = cbxPrivilegio.getSelectedItem().toString();
        }
        if (ctrLogin.validaLogin(txtLogin.getText(), String.valueOf(txtSenha.getPassword()), sPrivilegio) != null) {
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Usuário ou senha incorreta!");
        }
    }//GEN-LAST:event_btOKActionPerformed

